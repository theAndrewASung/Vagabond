<div class='heading'> vagabond multilingual journal @ cal</div>
 Vagabond is a UC Berkeley student-run multilingual literary journal that showcases creative works written in various languages accompanied by English translations. Our mission is to foster interest in multilingualism, promote diversity, and support multiculturalism in the UC Berkeley community by providing a space where creative works are published in their original forms -- free from distortions that may arise in the process of translation.
