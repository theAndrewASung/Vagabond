  Vagabond publishes original poetry and prose as well as visual works. All works are written by UC Berkeley students, professors, and staff members, or students from other universities who have an interest in sharing their work. Past issues have included works in Spanish, French, Italian, Japanese, Chinese, Hindi, Finnish, Polish, and much, much, more!
